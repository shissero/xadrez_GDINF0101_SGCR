// Esta função desenha um quadrado
// O canto inferior esquerdo do quadrado fica no ponto (x, y, 0.0)
void desenharQuadrado(float x, float y, float comprimento){

	glBegin(GL_POLYGON);
		glVertex3f(x, y, 0.0f);
		glVertex3f(x, y + comprimento, 0.0f);
		glVertex3f(x + comprimento, y + comprimento, 0.0f);
		glVertex3f(x + comprimento, y, 0.0f);
	glEnd();
}



