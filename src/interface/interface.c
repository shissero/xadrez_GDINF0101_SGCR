// Esta função desenha as casas do tabuleiro
// tam_casa é o comprimento das casas
void desenharCasas(float tam_casa){

	
	// Copia-se a matriz atual para descartar as alterações feitas pela função
	glPushMatrix();
			
	for(int linha = 0; linha < 8; linha++){

 		
 		// Copia-se a matriz novamente antes de desenhar cada linha de casas para simplificar o retorno ao início da linha no momento de desenhar a linha seguinte
 		glPushMatrix();
 			
 		for(int coluna = 0; coluna < 8; coluna++){
 		
	 		
	 		// Ajusta-se a cor de desenho para desenhar as casas
	 		// A soma das coordenadas de uma casa escura é sempre par
	 		// A soma das coordenadas de uma casa clara é sempre ímpar
	 		if( (linha + coluna) % 2 == 0 ){
	 			
	 			glColor4fv( CORES[CASA_ESCURA] );
	 		}
	 		else{
	 			
	 			glColor4fv( CORES[CASA_CLARA] );
	 		}
	 			
	 		
	 		
	 		// Desenha-se um quadrado
	 		desenharQuadrado(0.0, 0.0, tam_casa);
 				
 			
 			// Translada-se a origem do sistema para a direita, para desenhar a próxima casa da linha atual
 			glTranslatef(tam_casa, 0.0, 0.0);
 		}
 			
 		// Descarta-se as alterações para que a origem do sistema volte ao início da linha
 		glPopMatrix();
 		
 		
 		// Translada-se a origem do sistema para cima, para desenhar a próxima linha
 		glTranslatef(0.0, tam_casa, 0.0);
 	}
 		
 	// Descarta-se as alterações feitas pela função
 	glPopMatrix();
}



