// Esta função prepara o desenho: limpa o buffer de cor e cria o volume de projeção
void prepararDesenho(){

	
	// Inicializa-se as variáveis da interface
	inicializar();

	// Limpa-se o buffer de cor
	glClearColor(1.0, 1.0, 1.0, 1.0); 
	glClear(GL_COLOR_BUFFER_BIT);
	
	
	// Cria-se o volume de projeção
	glMatrixMode(GL_PROJECTION);
	glLoadIdentity();	
	glOrtho(TELA_TABULEIRO.esquerdo, TELA_TABULEIRO.direito, TELA_TABULEIRO.inferior, TELA_TABULEIRO.superior, TELA_TABULEIRO.proximal, TELA_TABULEIRO.distal);
	
	
	// Carrega-se a matriz de desenho
	glMatrixMode(GL_MODELVIEW);
	glLoadIdentity();
}
